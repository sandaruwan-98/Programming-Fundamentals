//display the summation,product,difference,devishion of two numbers
#include<stdio.h>
int main(){
	int num1,num2;
	
	//enter numbers
	printf("Enter two Numbers:");
	
	
	
	
	
	
	
	
	//input num1,num2
	scanf("%d %d",&num1,&num2);
	
	
	
	
	
	
	
	//total of two number
	int total=num1+num2;
	
	
	
	
	
	//Difference of two number
	int difference=num1-num2;
	
	
	
	
	
	//Multification(product)
	
	
	
	int product=num1*num2;
	
	
	
	
	//Devision
	float Devision=(float)num1/num2;
	
	//print Total
	printf("Total of Two Numbers: %d \n",total);
	
	//print Difference
	printf("Difference of Two Numbers: %d \n",difference);
	
	//print product(summation)
	printf("Product of Two Numbers(multification): %d\n",product);
	
	//print Devision
	printf("Devision of Two Numbers: %0.2f \n",Devision);
	
return 0;
	
}
